//Write a program that uses boolean literals to control program flow in an if-else statement.

public class BooleanLiterals {
    public static void main(String[] args) {
        
        boolean Language= true;
        boolean car= false;


        if (Language) {
            System.out.println("Language is fun to learn!");
        } else {
            System.out.println("Language is not fun.");
        }

        if (car) {
            System.out.println("The car is white!");
        } else {
            System.out.println("The car is not White, it's usually blue.");
        }

       
      
        
    }
}
