//Write a program to demonstrate the use of assignment operators (=, +=, -=, *=, /=, %=) on two integers.
class AassignmentOperators{

public static void main(String args[])
{
int a=100;

System.out.println(a=10);
System.out.println(a+=10);
System.out.println(a-=10);
System.out.println(a*=10);
System.out.println(a/=2);
System.out.println(a%=2);



}

} 
