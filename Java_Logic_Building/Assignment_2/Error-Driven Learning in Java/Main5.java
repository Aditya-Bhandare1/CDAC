public class Main5 { 
    public static void main(String[] args) { 
        System.out.println("Main method with String[] args"); 
    } 
    public static void main(int[] args) { 
        System.out.println("Overloaded main method with int[] args"); 
    } 
} 


/* output Main method with String[] args
The main method in Java is the entry point of any standalone Java application. It's where the program starts executing when you run the Java class.
we can have multiple Main methods with different parameters */